//Addition Module
function add(x,y)
{
    return x+y;
}

//Subtraction Module
function sub(x,y)
{
    return x-y;
}

//Multiplication Module
function mul(x,y)
{
    return x*y;
}

//Division Module
function div(x,y)
{
    return x/y;
}
module.exports=(add);
module.exports=(sub);
module.exports=(mul);
module.exports=(div);